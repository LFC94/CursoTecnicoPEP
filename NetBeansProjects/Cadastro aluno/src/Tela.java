    private void adicionarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarButtonActionPerformed
        aluno++;
        aluno1.setNome(nomeTextField.getText());
        aluno1.setIdade((idadeTextField.getText()));
        
        if (serieComboBox.getSelectedIndex() == 0) {
            aluno1.setClasse("1º ano");
           aluno1.setMensalidade( 120);

            mensalidadeFormat = formato.format(aluno1.getMensalidade());
        }
        if (serieComboBox.getSelectedIndex() == 1) {
             aluno1.setClasse("2º ano");
           aluno1.setMensalidade( 145);

            mensalidadeFormat = formato.format(aluno1.getMensalidade());
        }

        if (serieComboBox.getSelectedIndex() == 2) {
             aluno1.setClasse("3º ano");
           aluno1.setMensalidade( 168);

            mensalidadeFormat = formato.format(aluno1.getMensalidade());
        }
        if (serieComboBox.getSelectedIndex() == 3) {
             aluno1.setClasse("4º ano");
           aluno1.setMensalidade( 181);

            mensalidadeFormat = formato.format(aluno1.getMensalidade());
        }
       aluno1.setMensalidadeTotal( aluno1.getMensalidadeTotal()+aluno1.getMensalidade());
        mensalidadeTotalFormat = formato.format(aluno1.getMensalidadeTotal());
        mensalidatotalLabel.setText(mensalidadeTotalFormat);
        DefaultTableModel dados = (DefaultTableModel) dadosTable.getModel();
        dados.addRow(new String[]{aluno1.getNome(), aluno1.getClasse(),mensalidadeFormat, aluno1.getIdade()});
        TotalAlunoLabel.setText(String.valueOf(aluno));
    }//GEN-LAST:event_adicionarButtonActionPerformed

    private void removerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButtonActionPerformed
        String serie;
        if (dadosTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um nome na tabela");
        } else {
            
            aluno--;
            TotalAlunoLabel.setText(String.valueOf(aluno));
            serie = String.valueOf(dadosTable.getValueAt(dadosTable.getSelectedRow(), 1));
            
            if (serie.equalsIgnoreCase("1º ano")) {
              aluno1.setMensalidadeTotal(aluno1.getMensalidadeTotal()-120);
            }
            if (serie.equalsIgnoreCase("2º ano")) {
              aluno1.setMensalidadeTotal(aluno1.getMensalidadeTotal()-145);
                
            }
            if (serie.equalsIgnoreCase("3º ano")) {
                aluno1.setMensalidadeTotal(aluno1.getMensalidadeTotal()-168);
                
            }
            if (serie.equalsIgnoreCase("4º ano")) {
                aluno1.setMensalidadeTotal(aluno1.getMensalidadeTotal()-181);
                
            }
            mensalidadeTotalFormat = formato.format(aluno1.getMensalidadeTotal());
            mensalidatotalLabel.setText(mensalidadeTotalFormat);
            ((DefaultTableModel) dadosTable.getModel()).removeRow(dadosTable.getSelectedRow());
        }
    }//GEN-LAST:event_removerButtonActionPerformed

