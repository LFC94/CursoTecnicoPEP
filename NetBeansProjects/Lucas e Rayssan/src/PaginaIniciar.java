    private void car1RadioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_car1RadioButtonActionPerformed
        try {
            marcaComboBox.setSelectedIndex(car1.getMarca());
            modeloTextField.setText(car1.getModelo());
            anoTextField.setText(String.valueOf(car1.getAno()));
            cambio = car1.getCambio();
            if (cambio.equals("manual")) {
                manualRadioButton.setSelected(true);
            } else {

                if (cambio.equals("auto")) {
                    automaticoRadioButton.setSelected(true);
                } else {
                    cambiobuttonGroup.isSelected(null);
                }
            }
            if (car1.isArC()) {
                arCheckBox.setSelected(true);
            } else {
                arCheckBox.setSelected(false);
            }
            if (car1.isTravaE()) {
                travaCheckBox.setSelected(true);
            } else {
                travaCheckBox.setSelected(false);
            }
            if (car1.isVidroE()) {
                vidroCheckBox.setSelected(true);
            } else {
                vidroCheckBox.setSelected(false);
            }
        } catch (Exception e) {
            Mensagem("" + e);
        }
    }//GEN-LAST:event_car1RadioButtonActionPerformed

