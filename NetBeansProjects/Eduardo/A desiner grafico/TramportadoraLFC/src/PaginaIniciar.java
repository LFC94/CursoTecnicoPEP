    private void calcularButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularButtonActionPerformed
        if (PesoMercadoriaTextField.getText().trim().isEmpty()) {
           JOptionPane.showMessageDialog(null, 
                   "Digite o peso Errado!", "Atenção", 
                   JOptionPane.WARNING_MESSAGE);
           PesoMercadoriaTextField.requestFocus();
           return;
       }
        try {
        
            pesoMercadoria=Integer.parseInt(PesoMercadoriaTextField.getText());
                        
            if (SudesteRadioButton.isSelected()) {
                regiao=10;
            } 
            if (SulRadioButton.isSelected()) {
                   regiao=11;                
            }
            if (CentroOesteRadioButton.isSelected()) {
                regiao=12;
            }
            if(NordesteRadioButton.isSelected()){
                regiao=13;
            }
            if(NorteRadioButton.isSelected()){
                regiao=13;
            }
  
            valorSemExpresso=pesoMercadoria*regiao;
            valorPagar=valorSemExpresso;
            
            if (EncomendaExpressaCheckBox.isSelected()) {
                expresso=valorSemExpresso*0.06;
               valorPagar=expresso+valorSemExpresso; 
            }
            
            valorPessoFormatado=formato.format(valorPagar);
            valorTotalLabel.setText(valorPessoFormatado);
            
 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e,"Erro",JOptionPane.ERROR_MESSAGE);
       
        }
        
        
    }//GEN-LAST:event_calcularButtonActionPerformed

