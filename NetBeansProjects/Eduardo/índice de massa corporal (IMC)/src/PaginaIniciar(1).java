    private void CalcularIMCButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularIMCButtonActionPerformed

        try {

            //variaveis
            String nome, valorIMCFormatao;
            double peso, altura, valorIMC;
            //formatando
            DecimalFormat formato = new DecimalFormat("##.#");

            //variavel nome recebendo valor
            nome = NomeTextField.getText();

            //verificando se os campos peso e altura tem augum valor

            if (AlturaTextField.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo Autura e obrigatorio",
                        nome, JOptionPane.QUESTION_MESSAGE);
                AlturaTextField.requestFocus();
                return;
            }
            if (PesoTextField.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo Peso e obrigatorio",
                        nome, JOptionPane.QUESTION_MESSAGE);
                PesoTextField.requestFocus();
                return;
            }
            //variavel peso e altura recebendo valor
            peso = Double.parseDouble(PesoTextField.getText());
            altura = Double.parseDouble(AlturaTextField.getText());
            //calculando IMC
            valorIMC = peso / (altura * altura);
            //formatando resultado
            valorIMCFormatao = formato.format(valorIMC);
            //demotrando resultado
            ValorIMCLabel.setText(valorIMCFormatao);
            ValorIMCLabel.setForeground(new Color(0, 0, 0));

            if (valorIMC <=18.5) {
                MensagemLabel.setText(nome + " Você está abaixo do peso ideal");
            }

            if (valorIMC >=19 && valorIMC <=24.9) {
                MensagemLabel.setText("Parabéns " + nome + " Você está em seu peso normal!");
            }

            if (valorIMC>=25&&valorIMC<=29.9) {
                MensagemLabel.setText(nome + " Você está acima de seu peso");
            }
            
            if (valorIMC>=30&&valorIMC<=34.9) {
                MensagemLabel.setText(nome + " Você está com Obesidade grau I");
            }
            
            if (valorIMC>=35&&valorIMC<=39.9) {
                MensagemLabel.setText(nome + " Você está com Obesidade grau II");
            }

            if (valorIMC>=40) {
                MensagemLabel.setText(nome + " Você está com Obesidade grau III");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro " + e, "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_CalcularIMCButtonActionPerformed

