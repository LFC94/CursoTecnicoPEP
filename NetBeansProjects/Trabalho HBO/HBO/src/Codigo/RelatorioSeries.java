    private void RdB_GeneroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RdB_GeneroActionPerformed


        if (RdB_Descricao.isSelected()) {            
            Lbl_Titulo.setText("");
            Lbl_Titulo.enable(false);
            TxF_Texto.enable(false);
        } else {
            if (RdB_Genero.isSelected()) {
                Lbl_Titulo.enable(true);
                Lbl_Titulo.setText("Qual o Genero");
                TxF_Texto.enable(true);
            } else {
                if (RdB_Horario.isSelected()) {
                    Lbl_Titulo.enable(true);
                    Lbl_Titulo.setText("Qual o Horario");
                    TxF_Texto.enable(true);
                }
            }
        }


    }//GEN-LAST:event_RdB_GeneroActionPerformed

    private void btn_OkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_OkActionPerformed

        if (RdB_Descricao.isSelected()) {
            Qry_Series = conect.createQuery("SELECT s FROM Series s ORDER BY s.descricao");
        } else {
            if (RdB_Genero.isSelected()) {
                Qry_Series = conect.createQuery("SELECT s FROM Series s WHERE s.genero='" + TxF_Texto.getText() + "' ORDER BY s.descricao");
            } else {
                if (RdB_Horario.isSelected()) {
                    Qry_Series = conect.createQuery("SELECT s FROM Series s WHERE s.horario='" + TxF_Texto.getText() + "1 ORDER BY s.descricao");
                } else {
                    uteis.Mensagem("Seleciona um tipo de ordem");
                    return;
                }
            }
        }
        Series = Qry_Series.getResultList();
        try {
            JasperReport report = (JasperReport) JRLoader.loadObject("Jasper/Series.jasper");
            JasperPrint print = JasperFillManager.fillReport(report, null, new JRBeanCollectionDataSource(Series));
            JasperViewer visualizar = null;
            visualizar.setTitle("Relatorios das Series");
            visualizar.setExtendedState(visualizar.MAXIMIZED_BOTH);
            visualizar.setVisible(true);
        } catch (Exception e) {
            uteis.Mensagem("Erro: " + e);
        }
    }//GEN-LAST:event_btn_OkActionPerformed

