    private void adicionarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarButtonActionPerformed
     String tele;
        if (nomeTextField.getText().equals("")) {
            ms("Digite o nome");
            return;
        } else {
            registro1.setNome(nomeTextField.getText());
        }
        if (telefoneTextField.getText().equals("")) {
            ms("Pelo Menos o telefone é obrigatorio");
            
            return;
       
        } else {
            tele=telefoneTextField.getText();
            tele=tele.replaceAll("\\-","");
             if(tele.length()==8){
            if (dddTextField.getText().equals("")) {
                registro1.setTelefone(telefoneTextField.getText());
            } else {
                registro1.setTelefone(dddTextField.getText(), telefoneTextField.getText());
            }
        }else{
                 ms("Telefone incorreto");
                 return;
             }
            
        }
        DefaultTableModel dados = (DefaultTableModel) dadosTable.getModel();
        dados.addRow(new String[]{registro1.getNome(), registro1.getTelefone()});

    }//GEN-LAST:event_adicionarButtonActionPerformed

    private void removerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButtonActionPerformed
 
        if (dadosTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um nome na tabela");
        } else {
            ((DefaultTableModel) dadosTable.getModel()).removeRow(dadosTable.getSelectedRow());
        }
        
    }//GEN-LAST:event_removerButtonActionPerformed

    private void limparButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_limparButtonActionPerformed
     
        nomeTextField.setText("");
        dddTextField.setText("");
        telefoneTextField.setText("");
        
    }//GEN-LAST:event_limparButtonActionPerformed
